    private void setPictureIcon(String picture) {
        if (picture.length() > 2) {
            picture = picture.endsWith(".jpg") ? picture : (picture + ".jpg");
            img_full.setEnabled(true);
            img_remove.setEnabled(false);
            if (btn_Update.isSelected()) {
                img_remove.setEnabled(true);
            }
            BufferedImage img = ImageOptions.setResalution(176, 176, picture);
            // System.out.println("Set Picture" + picture);
            lblpic.setIcon(new ImageIcon(img));
            path = picture;
        } else {
            img_full.setEnabled(false);
            img_remove.setEnabled(false);
            BufferedImage img = ImageOptions.setResalution(176, 176, "07046d2f-bc6e-4291-b60f-a18e652f16f7.jpg");
            lblpic.setIcon(new ImageIcon(img));
            path = "";
        }
    }

    private void InsertPanels(int id) {
        insertcmbBox();
        TestsPojo model = new TestsSQL().getTestoPjo(id);
        setPictureIcon(model.getPicture());
        txt_id.setEnabled(false);
        txt_id.setText(String.valueOf(id));
        if (btn_Update.isSelected()) {
            img_up.setEnabled(true);
        }
        cmb_lang1.setSelectedItem(AllLanguage.getLanguage(model.getLang1()));
        cmb_lang2.setSelectedItem(AllLanguage.getLanguage(model.getLang2()));
        cmb_level.setSelectedItem(Level.getLevel(model.getLevel()));
        cmb_rate.setSelectedItem(Rate.getRating(model.getRating()));
        txt_score.setText(String.valueOf(model.getScore()));
        txt_username.setText(tbl_test.getModel().getValueAt(tbl_test.getSelectedRow(), 5).toString());
        txt_date.setText(tbl_test.getModel().getValueAt(tbl_test.getSelectedRow(), 8).toString());
        txt_desc.setText(model.getDescription());
        txt_first.setText(model.getQuestion());
        txt_second.setText(model.getAnswer());
        cmb_mode.setSelectedIndex(1);
        if (model.getMode() == 1 || model.getMode() == 3) {
            cmb_mode.setSelectedIndex(0);
        }
    }
