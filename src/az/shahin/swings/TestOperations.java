    private void start() {
        TestModel model = new TestModel(pojo.getId());
        if (searchresult != null) {
            model.start(searchresult);
        }
        lbl_mode.setVisible(false);
        cmb_mode.setVisible(false);
        if (Admin.isAdmin(pojo.getUsername())) {

            lbl_mode.setVisible(true);
            cmb_mode.setVisible(true);
        }
        tbl_test.removeAll();
        tbl_test.setModel(model);
        setTitle(pojo.getFirstname() + "'s Tests");
        lblUserDetails.setText("Welcome , " + pojo.getFirstname() + "  " + pojo.getLastname());
        group.add(btn_Show);
        group.add(btn_Update);
        group.add(btn_Delete);
        group.add(btn_Add);
        this.setSize(1010, 845);
        Thread thread = new Thread(new Runnable() {
            @Override
            public void run() {
                while (true) {
                    Calendar calendar = new GregorianCalendar();
                    String date = null;
                    date = date.format("%02d/%02d/%04d   %02d : %02d : %02d", calendar.getTime().getDate(), calendar.getTime().getMonth() + 1, calendar.getTime().getYear() + 1900, calendar.getTime().getHours(), calendar.getTime().getMinutes(), calendar.getTime().getSeconds());
                    lblTime.setText(date);
                    try {
                        Thread.sleep(500);
                    } catch (InterruptedException ex) {
                        //  Logger.getLogger(TestOperations.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        });
        thread.start();
        Training();
    }

