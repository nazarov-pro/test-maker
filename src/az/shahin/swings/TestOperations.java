    private void deleteTest() {
        if (tbl_test.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Select one item !", "ERROR", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String password = JOptionPane.showInputDialog(null, "Please type old password , and confirm operation", "Confirm Operation", JOptionPane.QUESTION_MESSAGE);
        if (password != null && password.equals(pojo.getPassword())) {
            new TestsSQL().DeleteData(Integer.parseInt(tbl_test.getModel().getValueAt(tbl_test.getSelectedRow(), 0).toString()));
            JOptionPane.showMessageDialog(this, "Sucessfully deleted", "OK", JOptionPane.PLAIN_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Password incorrect", "Wrong Password", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void updateTest() {
        if (txt_first.getText().trim().length() < 2) {
            JOptionPane.showMessageDialog(this, "First word's length is small", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (txt_second.getText().trim().length() < 2) {
            JOptionPane.showMessageDialog(this, "Second word's length is small", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (txt_desc.getText().trim().length() < 2) {
            JOptionPane.showMessageDialog(this, "Description's length is small", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            TestsPojo pojo = new TestsPojo();
            pojo.setLang1(cmb_lang1.getSelectedIndex());
            pojo.setLang2(cmb_lang2.getSelectedIndex());
            pojo.setLevel(cmb_level.getSelectedIndex());
            pojo.setDescription(txt_desc.getText());
            pojo.setQuestion(txt_first.getText());
            pojo.setAnswer(txt_second.getText());
            pojo.setAccount(this.pojo.getId());
            pojo.setId(Integer.parseInt(tbl_test.getModel().getValueAt(tbl_test.getSelectedRow(), 0).toString()));
            pojo.setPicture("");
            pojo.setMode(0);
            int modeprosda = 0;
            if (cmb_mode.getSelectedIndex() == 0) {
                modeprosda = 1;
            }
            if (path.length() > 4) {
                if (path.length() < 30) {
                    try {
                        String path2 = path;
                        path = GenerateUUID.getGeneratedUUID();
                        ImageOptions.copyFile(new File("img/" + (path2.endsWith(".jpg") ? (path2) : (path2 + ".jpg"))), path + ".jpg");
                    } catch (IOException ex) {
                        Logger.getLogger(ImageUpdate.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                    }
                }
                pojo.setPicture(path);
                if (cmb_mode.getSelectedIndex() == 0) {
                    modeprosda = 3;
                } else {
                    modeprosda = 2;
                }

            }
            pojo.setMode(modeprosda);
            Integer score = (txt_first.getText().length() + (cmb_level.getSelectedIndex() + 1)) * txt_second.getText().length();
            pojo.setScore(score);
            new TestsSQL().UpdateData(pojo);
            JOptionPane.showMessageDialog(this, "Sucessfully Updated", "OK", JOptionPane.PLAIN_MESSAGE);
        }
    }

