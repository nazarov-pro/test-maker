    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        int mode = 0;
        if (btn_Show.isSelected()) {
            SearchTests search = new SearchTests(this, true, pojo.getUsername());
            search.show();
            btn_Show.setSelected(true);
            if (search.getTests() != null || search.getTests().size() > 0) {
                searchresult = search.getTests();
            } else {
                searchresult = null;
            }
        } else if (tbl_test.getSelectedRow() != -1 && btn_Delete.isSelected()) {
            deleteTest();
            mode = 1;
        } else if (tbl_test.getSelectedRow() != -1 && btn_Update.isSelected()) {
            updateTest();
            mode = 2;
        } else if (btn_Add.isSelected()) {
            addTest();
            mode = 3;
        }
        start();
        btnsave.setVisible(true);
        if (mode == 0) {
            btn_ShowActionPerformed(evt);
        } else if (mode == 1) {
            btn_DeleteActionPerformed(evt);
        } else if (mode == 2) {
            btn_UpdateActionPerformed(evt);
        } else if (mode == 3) {
            btn_AddActionPerformed(evt);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnsaveActionPerformed

