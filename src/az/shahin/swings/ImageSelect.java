    private static String fileLocation = null;

    public static String getFileLocation() {
        return fileLocation;
    }
    private int count = 0;

    private void changePic() {
        if (!getimage.getNet()) {
            btnSelectActionPerformed(null);
            return;
        }
        try {
            ImageOptions.saveImage(path, searchkey + index);
        } catch (IOException ex) {
            
            if(count > 2){
                this.dispose();
                this.setVisible(false);
                fileLocation=null;
                return ;
            }else{
                JOptionPane.showMessageDialog(this, "File Not Found", "Error", JOptionPane.ERROR_MESSAGE);
                count++;
            }
            if (index > 0) {
                btnPrevActionPerformed(null);
            } else {
                btnNextActionPerformed(null);
            }
        } catch (Exception ex) {
            Logger.getLogger(ImageSelect.class.getName()).log(Level.SEVERE, null, ex);
        }
        Image icon = ImageOptions.setResalution(541, 460, searchkey + index + ".jpg");
        lblPic.setIcon(new ImageIcon(icon));
        if (index < 1) {
            btnPrev.setVisible(false);
        } else if (index > 2) {
            btnNext.setVisible(false);
        } else {
            btnPrev.setVisible(true);
            btnNext.setVisible(true);
        }

    }

    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        fileLocation = searchkey + index;
        this.dispose();
        this.setVisible(false);
    }//GEN-LAST:event_btnSelectActionPerformed

    private void btnPrevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrevActionPerformed
        index--;
        path = getimage.previousImageUrl(searchkey);
        changePic();
    }//GEN-LAST:event_btnPrevActionPerformed

