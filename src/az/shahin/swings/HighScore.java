    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (Admin.isAdmin(username)) {
            int index = lst_highScores.getSelectedIndex();
            if (index == -1) {
                JOptionPane.showMessageDialog(null, "Please , select person on the list");
                return;
            }
            highScoreSQL.Clear(idies[index]);
            start();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (Admin.isAdmin(username)) {
            int index = lst_highScores.getSelectedIndex();
            if (index == -1) {
                JOptionPane.showMessageDialog(null, "Please , select person on the list");
                return;
            }
            int result = JOptionPane.showConfirmDialog(null, "Do you want delete \"" + lst_highScores.getSelectedValue().toString() + "\" ?", "Confirm Menu", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (result == 0) {
                int myid = accountSQL.getAccountPojo(username).getId();
                if (myid == idies[index]) {
                    JOptionPane.showMessageDialog(null, "You are not remove this user ");
                } else {
                    accountSQL.DeleteData(idies[index]);
                    start();
                }

            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

