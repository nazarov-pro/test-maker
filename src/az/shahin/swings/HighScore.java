    public void start() {
        jButton1.setVisible(false);
        jButton2.setVisible(false);
        if (Admin.isAdmin(username)) {
            jButton1.setVisible(true);
            jButton2.setVisible(true);
        }
        List<HighScoresPojo> list = (List<HighScoresPojo>) highScoreSQL.getAll();
        HighScoresPojo mypojo = highScoreSQL.getUserScore(username);
        int myrating = 0;
        for (int i = 0; i < list.size(); i++) {
            if (list.get(i).getId() == mypojo.getId()) {
                myrating = i;
                break;
            }
        }
        lblWarning.setText("Your rating is " + (myrating + 1) + " and your score is " + mypojo.getScore());
        Iterator iterator = list.iterator();
        String Items[] = new String[list.size()];
        int i = 0;
        while (iterator.hasNext()) {
            HighScoresPojo pojo = (HighScoresPojo) iterator.next();
            AccountPojo acpojo = accountSQL.getAccountPojo(pojo.getId());
            idies[i] = pojo.getId();
            String item = "";
            Items[i] = item.format("%s   %s (Score : %d)", acpojo.getFirstname(), acpojo.getLastname(), pojo.getScore());
            barpojo.add(new BarCharPojo(acpojo.getFirstname() + " " + acpojo.getLastname(), pojo.getScore()));
            i++;
        }
        lst_highScores.setListData(Items);
    }
    private List<BarCharPojo> barpojo = new ArrayList<>();

