    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        lblWarning.setForeground(Color.red);
        lblWarning.setText("Welcome !");
        if (txtUser.getText().length() < 2) {
            lblWarning.setText("Please type full username !");
        } else if (txtPass.getText().length() < 2) {
            lblWarning.setText("Please type full password !");
        } else if (!accountSQL.Authorization(txtUser.getText(), txtPass.getText())) {
            lblWarning.setText("Invalid username(email) or password");
        } else {
            if (chk_remember.isSelected()) {
                if (!file.exists()) {
                    try {
                        file.createNewFile();
                    } catch (IOException ex) {
                        Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                try (PrintWriter writer = new PrintWriter(file)) {

                    writer.println(txtUser.getText());
                    writer.println(txtPass.getText());
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            EasyTestMaker easy = new EasyTestMaker(txtUser.getText());
            this.dispose();
            this.setVisible(false);
            easy.setVisible(true);
        }

    }//GEN-LAST:event_jButton1ActionPerformed
