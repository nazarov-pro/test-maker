    private void start() {
        this.setResizable(false);
        if (file.exists()) {
            try (Scanner scan = new Scanner(file)) {
                txtUser.setText(scan.nextLine());
                txtPass.setText(scan.nextLine());
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoSuchElementException ex) {
                file.deleteOnExit();
            }
        }
        showTips();
    }

    private void showTips() {
        ConfigPojo pojo = null;
        try {
            pojo = new XMLConfigReaderAndWriter().readData();
        } catch (IOException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SAXException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParserConfigurationException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (pojo != null) {
            pass = pojo.getAdminsPassword();
            if (pojo.isTip()) {
                LinkedList<String> buttons = new LinkedList<>();
                buttons.add("Close");
                buttons.add("Next");
                List<TestsPojo> listtips = getAllTips();
                int elementindex = new Random().nextInt(listtips.size());;
                TestsPojo pojok = listtips.get(elementindex);
                while (true) {
                    ImageIcon icon = new ImageIcon();
                    if (pojok.getPicture().length() > 10) {
                        icon = new ImageIcon(ImageOptions.setResalution(100, 100, pojok.getPicture()));
                    }
                    int input = JOptionPane.showOptionDialog(this, String.format("From ( %s ) \t %s\nTo ( %s ) \t %s\nDate : \t %s", AllLanguage.getLanguage(pojok.getLang1()), pojok.getQuestion(), AllLanguage.getLanguage(pojok.getLang2()), pojok.getAnswer(), pojok.getDate().toGMTString()), "Account ID : " + pojok.getAccount(), JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE, icon, buttons.toArray(), JOptionPane.CENTER_ALIGNMENT);
                    if (input == -1 || input == 0) {
                        return;
                    } else if (input == 1) {
                        elementindex = new Random().nextInt(listtips.size());
                    }

                    pojok = listtips.get(elementindex);
                }
            }
        }

    }

    private List<TestsPojo> getAllTips() {
        List<TestsPojo> listpojo = new TestsSQL().getAllData(-1, false);

        return listpojo;
    }

