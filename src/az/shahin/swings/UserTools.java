    private void deleteTest() {
        if (tbl_test.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Select one item !", "ERROR", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String password = JOptionPane.showInputDialog(null, "Please type old password , and confirm operation(You delete this user's account, this user's all tests,score)", "Confirm Operation", JOptionPane.QUESTION_MESSAGE);
        if (password != null && password.equals(pojo.getPassword())) {
            if (Admin.isAdmin(txt_username.getText())) {
                int i = JOptionPane.showConfirmDialog(this, "This user is Admin!Do you want continue this process ?", "Confirmation", JOptionPane.WARNING_MESSAGE);
                if (i != 0) {
                    return;
                }
            }
            new AccountSQL().DeleteData(Integer.parseInt(tbl_test.getModel().getValueAt(tbl_test.getSelectedRow(), 0).toString()));
            UserModel model = new UserModel(Admin.isAdmin(pojo.getUsername()));
            if (SearchResult != null && SearchResult.size() > 0) {
                model.setData(SearchResult);
            }
            tbl_test.setModel(model);
            JOptionPane.showMessageDialog(this, "Sucessfully deleted", "OK", JOptionPane.PLAIN_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Password incorrect", "Wrong Password", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void updateTest() {

        lblWarning.setText("");
        lblWarning.setForeground(Color.red);
        if (txt_first.getText().length() < 3 || txt_first.getText().length() > 19) {
            lblWarning.setText("FirstName is too small (or large) ");
        } else if (txt_second.getText().length() < 3 || txt_second.getText().length() > 19) {
            lblWarning.setText("LastName is too small (or large) ");
        } else if (txt_password.getText().length() < 5 || txt_password.getText().length() > 19) {
            lblWarning.setText("Password is too small (or large) ");
        } else if (!txt_password.getText().equals(txt_password.getText())) {
            lblWarning.setText("Again type password error");
        } else if (txt_mail.getText().length() > 34 || !Validator.isValidEmailAddress(txt_mail.getText())) {
            lblWarning.setText("Invalid Email adress  (or large) ");
        } else if (!String.valueOf(tbl_test.getModel().getValueAt(tbl_test.getSelectedRow(), 8)).equals(txt_mail.getText()) && new AccountSQL().RegistrationCheck(txt_mail.getText(), false) == 1) {
            lblWarning.setText("This Email already exist");
        } else if (!Validator.isValidNumber(txt_tel.getText()) || txt_tel.getText().length() > 13) {
            lblWarning.setText("Invalid Telephone number (or large) ");
        } else {
            String password = JOptionPane.showInputDialog(null, "Please type old password", "Confirm Password", JOptionPane.QUESTION_MESSAGE);
            if (password != null && password.equals(pojo.getPassword())) {
                AccountPojo pojoec = new AccountPojo(Integer.parseInt(txt_id.getText()), txt_username.getText(), txt_first.getText(), txt_second.getText(), txt_tel.getText(), txt_mail.getText(), txt_password.getText(), cmb_gender.getSelectedIndex() == 0 ? 'M' : 'F', cmb_status.getSelectedIndex() == 0 ? 8456565 : 0, null);
                new AccountSQL().UpdateData(pojoec);
                if (!String.valueOf(tbl_test.getModel().getValueAt(tbl_test.getSelectedRow(), 0)).equals(txt_score.getText())) {
                    int number = 0;
                    try {
                        number = Integer.parseInt(txt_score.getText());
                    } catch (Exception ex) {

                    }
                    new HighScoreSQL().Update(txt_username.getText(), number);
                }
                UserModel model = new UserModel(Admin.isAdmin(pojo.getUsername()));
                if (SearchResult != null && SearchResult.size() > 0) {
                    model.setData(SearchResult);
                }
                tbl_test.setModel(model);
                JOptionPane.showMessageDialog(this, "Sucessfully Updated", "OK", JOptionPane.PLAIN_MESSAGE);
            }
            lblWarning.setText("Invalid password !");
        }

    }

