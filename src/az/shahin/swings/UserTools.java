    private void InsertPanels(int row) {
        txt_id.setText(String.valueOf(tbl_test.getModel().getValueAt(row, 0)));
        txt_first.setText(String.valueOf(tbl_test.getModel().getValueAt(row, 1)));
        txt_second.setText(String.valueOf(tbl_test.getModel().getValueAt(row, 2)));
        if (tbl_test.getModel().getValueAt(row, 3).equals("Male")) {
            cmb_gender.setSelectedIndex(0);
        } else {
            cmb_gender.setSelectedIndex(1);
        }
        txt_tel.setText(String.valueOf(tbl_test.getModel().getValueAt(row, 4)));
        txt_score.setText(String.valueOf(tbl_test.getModel().getValueAt(row, 5)));
        if (Admin.isAdmin(pojo.getUsername())) {
            txt_username.setText(String.valueOf(tbl_test.getModel().getValueAt(row, 6)));
            txt_password.setText(String.valueOf(tbl_test.getModel().getValueAt(row, 7)));
            txt_mail.setText(String.valueOf(tbl_test.getModel().getValueAt(row, 8)));
            if (tbl_test.getModel().getValueAt(row, 9).equals("Admin")) {
                cmb_status.setSelectedIndex(0);
            } else {
                cmb_status.setSelectedIndex(1);
            }
            lbl_date.setText("Date : " + String.valueOf(tbl_test.getModel().getValueAt(row, 10)));
        } else {
            lbl_date.setText("Date : " + String.valueOf(tbl_test.getModel().getValueAt(row, 6)));
        }

    }

